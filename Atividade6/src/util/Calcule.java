package util;

public class Calcule {

	public static int soma(int primeiro, int segundo) {
		return (primeiro + segundo);
	}

	public static int somaTres(int primeiro, int segundo, int terceiro) {
		return (primeiro + segundo + terceiro);
	}

	public static int subtracaoDois(int primeiro, int segundo) {
		return (primeiro - segundo);
	}

	public static int subtracaoTres(int primeiro, int segundo, int terceiro) {
		return (primeiro - segundo - terceiro);
	}

	public static int multiplicaDois(int primeiro, int segundo) {
		return (primeiro * segundo);
	}

	public static int multiplicaTres(int primeiro, int segundo, int terceiro) {
		return (primeiro * segundo * terceiro);
	}

	public static int divideDois(int primeiro, int segundo) {
		return (primeiro / segundo);
	}

	public static int divideTres(int primeiro, int segundo, int terceiro) {
		return (primeiro / segundo / terceiro);
	}
}
