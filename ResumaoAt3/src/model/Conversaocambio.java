package model;

public class Conversaocambio {
	public float dolar;
	public float real;

	public void setDolar(float dolar) {
		this.dolar = dolar;
	}

	public float getDolar() {
		return this.dolar;
	}

	public void setReal(float real) {
		this.real = real;
	}

	public float getReal() {
		return this.real;
	}

	public Conversaocambio() {

	}

	public Conversaocambio(float dolar, float real) {
		this.dolar = dolar;
		this.real = real;
	}

}
